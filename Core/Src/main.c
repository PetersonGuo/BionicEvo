/*
 * @brief Maps potentiometer value (0-4095) to servo angle (0-180)
 * @param val potentiometer value
 * @retval int Servo angle
 */
uint8_t pot_serv_map(uint16_t val) {
	return val/4095.0*180;
}

/*
 * @brief Takes in an angle and returns a PWM vaue, Restricts angles to 0-180 for this use case
 * @param angle Desired servo angle
 * @retval int PWM value
 */
uint16_t serv_angle(uint8_t angle) {
	if (angle > 180) {
		angle = 180;
	} else if (angle < 60) {
		angle = 40;
	}

	return BASE + INC*angle;
}

/*
 * @brief Pushes values to front of array
 * @param arr The array to push to
 * @param val The array vals to push
 * @param size1 The first array size
 * @param size2 The second array size
 */
void push_front(uint16_t arr[ADC_SIZE][NUM_PAST_VAL], uint16_t val[ADC_SIZE], size_t size1, size_t size2) {
	for (size_t i = 0; i < ADC_SIZE; ++i) {
		for (size_t j = NUM_PAST_VAL-1; j > 0; --j) {
			arr[i][j] = arr[i][j-1];
		}
		arr[i][0] = val[i];
	}
}

/*
 * Testing function for demo tests
 */
void test(uint8_t runtime) {
	for (uint8_t i = 0; i < runtime; ++i) {
		__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_3, serv_angle(0));
		__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_2, serv_angle(0));
		__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2, serv_angle(0));
		__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1, serv_angle(0));
		__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_3, serv_angle(0));
		HAL_Delay(1000);
		__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_3, serv_angle(180));
		__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_2, serv_angle(180));
		__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2, serv_angle(180));
		__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1, serv_angle(180));
		__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_3, serv_angle(180));
		HAL_Delay(1000);
	}
}

/*
 * Main run function
 */
void run(void) {
	HAL_ADC_Start_DMA(&hadc1,(uint32_t *) rawValues, ADC_SIZE);
	while (!convCompleted);

	push_front(pastValues,rawValues,NUM_PAST_VAL,ADC_SIZE);

	__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_3,serv_angle(pot_serv_map(rawValues[0])));
	__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_2,serv_angle(pot_serv_map(rawValues[1])));
  	__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,serv_angle(pot_serv_map(rawValues[2])));
  	__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,serv_angle(pot_serv_map(rawValues[3])));

  	sprintf(msg,"%d %d %d %d\r\n", pastValues[0][0], pastValues[1][0], pastValues[2][0], pastValues[3][0]);
  	HAL_UART_Transmit(&huart2, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);
  	HAL_Delay(100);
}

/*
 * Set all servos to the same angle
 */
void set_all_servs(uint8_t angle) {
	__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_3,serv_angle(angle));
	__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_2,serv_angle(angle));
	__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,serv_angle(angle));
	__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,serv_angle(angle));
}
